/// EJEMPLOS DEL ORDEN DE LOS PARAMETROS

void sumarDosInt(int numeroA, int numeroB, int* resultado)
{
    *resultado = numeroA + numeroB;
}

void sumarDosFloats(float numeroA, float* resultado, float numeroB)
{
    *resultado = numeroA + numeroB;
}

void sumarDosDoubles(double* resultado, double numeroA, double numeroB)
{
    *resultado = numeroA + numeroB;
}
